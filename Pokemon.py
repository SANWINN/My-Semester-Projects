def CharChallengeGame():
    while True:
        print("""
            Choose an option below!""")
        print("""1. Train
2. Gym Battle
3. Display Pokemon Info
4. Exit""")
        option = int(input("1-4: "))

        if option == 1:
            global pokemon_level
            pokemon_level = pokemon_level + 1
            print ("Your pokemon level is " + str(pokemon_level))

        if option == 2:
            pokemonDamage = random.randint(1000,2000)
            enemyDamage = random.randint(1000,2000)
            if pokemonDamage > enemyDamage:
                print("You won the battle!")
                pokemon_level = pokemon_level + 2
            else:
                print("Oh no! You lost!")

        if option == 3:
            if pokemon_level < 5:
                pokemon_name = "Charmander"
            if pokemon_level >= 5 and pokemon_level < 10:
                pokemon_name = "Charmeleon"
            if pokemon_level >= 10:
                pokemon_name = "Charizard"
            if pokemon_name == "Charmander":
                pokemon_image = drawBabyCharmander()
            if pokemon_name == "Charmeleon":
                pokemon_image = drawTeenCharmeleon()
            if pokemon_name == "Charizard":
                pokemon_image = drawAdultCharizard()

            print("Here is your Pokemon information!")
            print("Pokemon Name: " + pokemon_name)
            print("Pokemon Level: " + str(pokemon_level))
            print("Here's your Pokemon image above!")

        if option == 4:
            print("Thank you for play the Char-Challenge Game!")
            break

